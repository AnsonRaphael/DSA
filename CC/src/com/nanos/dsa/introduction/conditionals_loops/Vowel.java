package com.nanos.dsa.introduction.conditionals_loops;

public class Vowel {
    public static void main(String[] args) {
        char c='k';
        if(c=='a'||c=='A'||c=='e'||c=='E'||c=='i'||c=='I'||c=='o'||c=='O'||c=='u'||c=='U'){
            System.out.println("Vowel");
        }else {
            System.out.println("Consonant");
        }
    }
}
